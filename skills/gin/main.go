package main

import (
	"fmt"
	"net/http"
	"os"

	"github.com/gin-gonic/gin"
)

func main() {
	r := gin.Default()

	r.GET("/hello", func(c *gin.Context) {
		c.JSON(http.StatusOK, gin.H{
			"code": 200,
			"msg":  "hello world",
		})
	})

	if err := r.Run(":8081"); err != nil {
		fmt.Println(err)
		os.Exit(1)
	}
}
